package org.wrapperClass;

public class Wrapperclassexample {

    public static void main(String[] args) {
        byte b=10;
        short s=20;
        int i=30;
        long l=40;
        float f=50.0F;
        double d=60.0D;
        char c='a';
        boolean b2=true;

        Byte a = b;
        System.out.println(a);
        Short shortObject = s;
        System.out.println(shortObject);
        Integer intObject = i;
        System.out.println(intObject);
        Long lonObject = l;
        System.out.println(lonObject);
        Float floatObject = f;
        System.out.println(floatObject);
        Double doubleObject = d;
        System.out.println(doubleObject);
        Character chrObject = c;
        System.out.println(chrObject);
        Boolean booleanObject = b2;
        System.out.println(booleanObject);

    }
}
