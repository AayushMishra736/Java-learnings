package org.math;

public class Mathclassexample {

    public static void main(String[] args) {
        double x = 34;
        double y = 90;
        double z = 100.15;

        System.out.println(Math.abs(z));

        System.out.println(Math.max(90,34));

        System.out.println(Math.min(x,y));

        System.out.println(Math.round(z));

        System.out.println(Math.sqrt(x));

        System.out.println(Math.cbrt(y));

        System.out.println(Math.pow(y,x));

        System.out.println(Math.signum(x));

        System.out.println(Math.ceil(z));

    }

}
